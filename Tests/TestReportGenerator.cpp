void TestReportGenerator::writeXmlTestCase(QXmlStreamWriter& writer, const TestResult& result)
{
    writer.writeStartElement("testcase");
    writer.writeAttribute("name", result.testName);
    writer.writeAttribute("classname", result.category);
    writer.writeAttribute("time", QString::number(result.durationMs / 1000.0));
    
    if (!result.passed) {
        writer.writeStartElement("failure");
        writer.writeAttribute("message", result.errorMessage);
        writer.writeCharacters(result.errorMessage);
        writer.writeEndElement(); // failure
    }
    
    writer.writeEndElement(); // testcase
}

