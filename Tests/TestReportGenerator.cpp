TestReportGenerator::TestReportGenerator(QObject *parent)
    : QObject(parent)
{
    // Set default output directory
    m_outputDirectory = QStandardPaths::writableLocation(QStandardPaths::DocumentsLocation) + "/GlanceTestReports";
    QDir().mkpath(m_outputDirectory);
    
    m_reportTitle = "Glance Test Report";
    
    // Collect system information
    QJsonObject systemInfo;
    systemInfo["os"] = QSysInfo::prettyProductName();
    systemInfo["kernel"] = QSysInfo::kernelVersion();
    systemInfo["architecture"] = QSysInfo::currentCpuArchitecture();
    systemInfo["hostname"] = QSysInfo::machineHostName();
    systemInfo["qt_version"] = QT_VERSION_STR;
    
    m_environmentInfo = systemInfo;
}

