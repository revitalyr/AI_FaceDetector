void TestReportGenerator::addTestResult(const TestResult& result)
{
    m_testResults.append(result);
    
    if (m_suiteActive) {
        m_currentSuite.totalTests++;
        if (result.passed) {
            m_currentSuite.passedTests++;
        } else {
            m_currentSuite.failedTests++;
        }
    }
}

void TestReportGenerator::addBenchmarkResult(const TestBenchmarkResult& result)
{
    m_benchmarkResults.append(result);
}

void TestReportGenerator::startTestSuite(const QString& suiteName)
{
    m_currentSuite = TestSuiteSummary();
    m_currentSuite.suiteName = suiteName;
    m_currentSuite.startTime = QDateTime::currentDateTime();
    m_currentSuite.environment = QSysInfo::prettyProductName();
    m_currentSuite.systemInfo = m_environmentInfo;
    
    m_suiteStartTime = QDateTime::currentDateTime();
    m_suiteActive = true;
}

void TestReportGenerator::endTestSuite()
{
    if (m_suiteActive) {
        m_currentSuite.endTime = QDateTime::currentDateTime();
        m_currentSuite.totalDurationMs = m_suiteStartTime.msecsTo(m_currentSuite.endTime);
        m_currentSuite.successRate = calculateSuccessRate();
        
        m_suiteActive = false;
    }
}

bool TestReportGenerator::generateJsonReport()
{
    QJsonObject report;
    report["title"] = m_reportTitle;
    report["generated_at"] = QDateTime::currentDateTime().toString(Qt::ISODate);
    report["environment"] = m_environmentInfo;
    
    // Add test suite summary
    QJsonObject suiteSummary;
    suiteSummary["name"] = m_currentSuite.suiteName;
    suiteSummary["start_time"] = m_currentSuite.startTime.toString(Qt::ISODate);
    suiteSummary["end_time"] = m_currentSuite.endTime.toString(Qt::ISODate);
    suiteSummary["total_tests"] = m_currentSuite.totalTests;
    suiteSummary["passed_tests"] = m_currentSuite.passedTests;
    suiteSummary["failed_tests"] = m_currentSuite.failedTests;
    suiteSummary["skipped_tests"] = m_currentSuite.skippedTests;
    suiteSummary["total_duration_ms"] = m_currentSuite.totalDurationMs;
    suiteSummary["success_rate"] = m_currentSuite.successRate;
    
    report["test_suite"] = suiteSummary;
    
    // Add test results
    QJsonArray testResults;
    for (const auto& result : m_testResults) {
        QJsonObject testResult;
        testResult["name"] = result.testName;
        testResult["category"] = result.category;
        testResult["passed"] = result.passed;
        testResult["duration_ms"] = result.durationMs;
        testResult["timestamp"] = result.timestamp.toString(Qt::ISODate);
        
        if (!result.errorMessage.isEmpty()) {
            testResult["error"] = result.errorMessage;
        }
        
        if (!result.warnings.isEmpty()) {
            QJsonArray warnings;
            for (const QString& warning : result.warnings) {
                warnings.append(warning);
            }
            testResult["warnings"] = warnings;
        }
        
        if (!result.metrics.isEmpty()) {
            testResult["metrics"] = result.metrics;
        }
        
        testResults.append(testResult);
    }
    report["test_results"] = testResults;
    
    // Add benchmark results
    QJsonArray benchmarkResults;
    for (const auto& result : m_benchmarkResults) {
        QJsonObject benchmark;
        benchmark["name"] = result.testName;
        benchmark["value"] = result.value;
        benchmark["unit"] = result.unit;
        benchmark["category"] = result.category;
        benchmark["timestamp"] = result.timestamp.toString(Qt::ISODate);
        
        if (!result.details.isEmpty()) {
            benchmark["details"] = result.details;
        }
        
        benchmarkResults.append(benchmark);
    }
    report["benchmark_results"] = benchmarkResults;
    
    // Add analysis
    report["analysis"] = analyzeTestResults();
    
    // Generate JSON document
    QJsonDocument doc(report);
    QString jsonContent = doc.toJson();
    
    QString filePath = getReportFilePath("json");
    bool success = writeToFile(filePath, jsonContent);
    
    if (success) {
        reportGenerated("JSON", filePath);
    }
    
    return success;
}

bool TestReportGenerator::generateHtmlReport()
{
    QString html = HTML_TEMPLATE;
    
    // Replace placeholders
    html.replace("{TITLE}", m_reportTitle);
    html.replace("{TIMESTAMP}", QDateTime::currentDateTime().toString());
    html.replace("{CSS_STYLES}", CSS_STYLES);
    
    // Generate sections
    html.replace("{SUMMARY_SECTION}", generateHtmlSummary());
    html.replace("{TEST_RESULTS_SECTION}", generateHtmlTestResults());
    html.replace("{BENCHMARK_RESULTS_SECTION}", generateHtmlBenchmarkResults());
    html.replace("{CHARTS_SECTION}", generateHtmlCharts());
    html.replace("{ANALYSIS_SECTION}", generateHtmlAnalysis());
    
    // Generate JavaScript with actual data
    QString js = JAVASCRIPT_CHARTS;
    js.replace("{PASSED_COUNT}", QString::number(m_currentSuite.passedTests));
    js.replace("{FAILED_COUNT}", QString::number(m_currentSuite.failedTests));
    js.replace("{SKIPPED_COUNT}", QString::number(m_currentSuite.skippedTests));
    
    // Add chart data
    QStringList performanceLabels;
    QList<double> performanceData;
    for (const auto& result : m_testResults) {
        performanceLabels << result.testName;
        performanceData << result.durationMs;
    }
    
    js.replace("{PERFORMANCE_LABELS}", QString("[%1]").arg(performanceLabels.join(",")));
    QStringList stringData;
    for (double val : performanceData) {
        stringData << QString::number(val);
    }
    js.replace("{PERFORMANCE_DATA}", QString("[%1]").arg(stringData.join(",")));
    
    html.replace("{JAVASCRIPT_CHARTS}", js);
    
    QString filePath = getReportFilePath("html");
    bool success = writeToFile(filePath, html);
    
    if (success) {
        reportGenerated("HTML", filePath);
    }
    
    return success;
}

bool TestReportGenerator::generateXmlReport()
{
    QString xmlContent;
    QXmlStreamWriter writer(&xmlContent);
    writer.setAutoFormatting(true);
    writer.writeStartDocument();
    
    // Write test suite
    writer.writeStartElement("testsuite");
    writer.writeAttribute("name", m_currentSuite.suiteName);
    writer.writeAttribute("tests", QString::number(m_currentSuite.totalTests));
    writer.writeAttribute("failures", QString::number(m_currentSuite.failedTests));
    writer.writeAttribute("errors", "0");
    writer.writeAttribute("skipped", QString::number(m_currentSuite.skippedTests));
    writer.writeAttribute("time", QString::number(m_currentSuite.totalDurationMs / 1000.0));
    writer.writeAttribute("timestamp", m_currentSuite.startTime.toString(Qt::ISODate));
    
    // Write test cases
    for (const auto& result : m_testResults) {
        writeXmlTestCase(writer, result);
    }
    
    writer.writeEndElement(); // testsuite
    writer.writeEndDocument();
    
    QString filePath = getReportFilePath("xml");
    bool success = writeToFile(filePath, xmlContent);
    
    if (success) {
        reportGenerated("XML", filePath);
    }
    
    return success;
}

