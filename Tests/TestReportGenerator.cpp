QJsonObject TestReportGenerator::compareWithBaseline(const QString& baselineFile)
{
    QJsonObject comparison;
    
    // Load baseline file
    QFile file(baselineFile);
    if (!file.open(QIODevice::ReadOnly)) {
        comparison["error"] = "Could not open baseline file";
        return comparison;
    }
    
    QJsonDocument baselineDoc = QJsonDocument::fromJson(file.readAll());
    QJsonObject baseline = baselineDoc.object();
    
    // Compare test results
    if (baseline.contains("test_results")) {
        QJsonArray baselineTests = baseline["test_results"].toArray();
        
        // Create map of baseline results
        QMap<QString, QJsonObject> baselineMap;
        for (const auto& value : baselineTests) {
            QJsonObject test = value.toObject();
            baselineMap[test["name"].toString()] = test;
        }
        
        // Compare with current results
        QJsonArray differences;
        for (const auto& result : m_testResults) {
            if (baselineMap.contains(result.testName)) {
                QJsonObject baselineTest = baselineMap[result.testName];
                
                // Check for status change
                bool baselinePassed = baselineTest["passed"].toBool();
                if (baselinePassed != result.passed) {
                    QJsonObject diff;
                    diff["test"] = result.testName;
                    diff["type"] = "status_change";
                    diff["from"] = baselinePassed ? "passed" : "failed";
                    diff["to"] = result.passed ? "passed" : "failed";
                    differences.append(diff);
                }
                
                // Check for performance regression
                qint64 baselineTime = baselineTest["duration_ms"].toVariant().toLongLong();
                if (result.durationMs > baselineTime * 1.2) { // 20% slower
                    QJsonObject diff;
                    diff["test"] = result.testName;
                    diff["type"] = "performance_regression";
                    diff["baseline_ms"] = baselineTime;
                    diff["current_ms"] = result.durationMs;
                    diff["regression_percent"] = ((result.durationMs - baselineTime) * 100.0) / baselineTime;
                    differences.append(diff);
                }
            }
        }
        
        comparison["differences"] = differences;
    }
    
    return comparison;
}

QStringList TestReportGenerator::detectRegressions()
{
    QStringList regressions;
    
    // Simple regression detection based on test failures
    for (const auto& result : m_testResults) {
        if (!result.passed) {
            regressions << QString("Test failed: %1 - %2").arg(result.testName, result.errorMessage);
        }
    }
    
    return regressions;
}

QStringList TestReportGenerator::detectPerformanceRegressions(double threshold)
{
    QStringList regressions;
    
    // Group tests by category and calculate average performance
    QMap<QString, QList<double>> categoryTimes;
    for (const auto& result : m_testResults) {
        categoryTimes[result.category].append(result.durationMs);
    }
    
    // Check for performance issues
    for (auto it = categoryTimes.begin(); it != categoryTimes.end(); ++it) {
        QString category = it.key();
        QList<double> times = it.value();
        
        if (times.size() > 1) {
            // Calculate average
            double sum = 0;
            for (double time : times) {
                sum += time;
            }
            double average = sum / times.size();
            
            // Check if any test is significantly slower than average
            for (double time : times) {
                if (time > average * (1 + threshold)) {
                    regressions << QString("Performance regression in %1: %2ms (avg: %3ms)")
                                   .arg(category).arg(time, 0, 'f', 2).arg(average, 0, 'f', 2);
                }
            }
        }
    }
    
    return regressions;
}

